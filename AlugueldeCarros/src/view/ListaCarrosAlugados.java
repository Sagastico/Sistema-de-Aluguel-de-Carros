     public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) ListaCarrosAlugados.getModel();
        modelo.setNumRows(0);
        CarrosDAO dao = new CarrosDAO();

        for (Carros p : dao.readCarrosClientes()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getCarro(),
                p.getPlaca(),
                p.getUsuario(),
                p.getCpf(),
                p.getHorario(),
                p.getHorasalugadas(),
            });

        }
    }

