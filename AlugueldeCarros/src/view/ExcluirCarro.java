    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) TabelaCarros.getModel();
        modelo.setNumRows(0);
        CarrosDAO dao = new CarrosDAO();

        for (Carros p : dao.readCarrosExclusao()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getCarro(),
                p.getPlaca(),
            });

        }
    }
